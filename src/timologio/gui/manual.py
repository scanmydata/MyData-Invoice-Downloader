def build_manual(target: Path) -> Path:
    """Γράφει το εγχειρίδιο. Απαιτεί ενεργό QGuiApplication (offscreen αρκεί)."""
    target.parent.mkdir(parents=True, exist_ok=True)

    writer = QPdfWriter(str(target))
    writer.setPageSize(QPageSize(QPageSize.PageSizeId.A4))
    writer.setPageMargins(QMarginsF(16, 14, 16, 14), QPageLayout.Unit.Millimeter)
    writer.setTitle("Εγχειρίδιο χρήσης — Λήψη Παραστατικών myDATA")
    writer.setCreator("Λήψη Παραστατικών myDATA")
    writer.setResolution(96)

    doc = QTextDocument()
    doc.setHtml(_html())
    # Χωρίς αυτό το Qt στοιχειοθετεί σε πλάτος οθόνης και το κείμενο βγαίνει
    # έξω από το χαρτί.
    page = writer.pageLayout().paintRectPixels(writer.resolution())
    doc.setPageSize(QSizeF(page.size()))
    doc.print_(writer)
    return target


def ensure_manual(data_dir: Path) -> Path:
    """Η διαδρομή του εγχειριδίου, φτιάχνοντάς το αν λείπει ή αν άλλαξε.

    Γράφεται στον φάκελο δεδομένων και όχι δίπλα στο εκτελέσιμο: το Program
    Files δεν είναι εγγράψιμο για απλό χρήστη.

    Κρατάμε δίπλα του την υπογραφή του κειμένου: χωρίς αυτό, ένα εγχειρίδιο που
    φτιάχτηκε μια φορά δεν ξαναχτιζόταν ποτέ, οπότε όποια αλλαγή γράφαμε εδώ
    δεν έφτανε ποτέ σε όποιον είχε ήδη ανοίξει το PDF. Η σύγκριση γίνεται με
    hash του περιεχομένου (και όχι με ημερομηνίες): μέσα στο bundle του
    PyInstaller τα αρχεία ξεπακετάρονται με νέα ημερομηνία σε κάθε εκτέλεση.
    """
    target = data_dir / FILENAME
    stamp = data_dir / ".manual.hash"
    signature = hashlib.sha256(_html().encode("utf-8")).hexdigest()

    if target.exists() and stamp.exists():
        try:
            if stamp.read_text(encoding="utf-8").strip() == signature:
                return target
        except OSError:
            pass

    build_manual(target)
    try:
        stamp.write_text(signature, encoding="utf-8")
    except OSError:
        # Χωρίς τη σφραγίδα απλώς θα ξαναχτίζεται — δεν είναι λόγος αποτυχίας.
        pass
    return target
